    public static String getRandomString(int length) {
        String base = "abcdefghijklmnopqrstuvwxyz0123456789";
        Random random = new Random();
        StringBuffer sb = new StringBuffer();
        for (int i = 0; i < length; i++) {
            int number = random.nextInt(base.length());
            sb.append(base.charAt(number));
        }
        return sb.toString();
    }

    // 鉴权
    public static String generateAppSign(TreeMap<String, String> mParams) throws UnsupportedEncodingException {
        Set<String> keySet = mParams.keySet();
        StringBuilder sb = new StringBuilder();
        Iterator<String> iterator = keySet.iterator();
        while (iterator.hasNext()) {
            String key = iterator.next();
            String value = mParams.get(key);
            sb.append("&").append(key).append("=").append(URLEncoder.encode(value, "UTF-8"));
        }
        sb.deleteCharAt(0);
        sb.append("&app_key=").append(Constant.Tencent_AppKey);
        String sign = Md5Util.md5(sb.toString()).toUpperCase();
        return sign;
    }

    // 将文件转为Base64
    public static String fileToBase64(File file) {
        String res;
        try {
            res = encode(readFileByBytes(file));
        } catch (Exception e) {
            return null;
        }
        return res;
    }

